package com.yedam.java.example;

public class Calculator {
	
	int plus(int x,int y) {
		int result = (int)x + (int) y;
		return result;
	}
	int minus(int x,int y) {
		return x-y;
	}
	int mulit(int x, int y) {
		return x*y;
	}
	double divide(double x,double y) {
		return x / y;
	}

}
